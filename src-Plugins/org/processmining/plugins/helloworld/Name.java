	class Name {
	  private String first, last;
	 
	  // Constructor
	  public Name(String first, String last) {
	    this.first = first;
	    this.last = last;
	  }
	 
	  // Getters
	  public String getFirst() {
	    return first;
	  }
	 
	  public String getLast() {
	    return last;
	  }
	}